#include<iostream>

int main(){
    int students = 20;

    // increment 
    students = students + 1; // 21
    students++; // 22 
    students+=1; // 23

    std::cout << students << "\n";

    // decrement
    students = 20;
    
    students = students - 1; // 19
    students--; // 18
    students -= 1; // 17 

    std::cout << students;


    // multiplication
    students = 2;
    

    return 0;
}